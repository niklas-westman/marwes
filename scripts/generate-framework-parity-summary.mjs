import { readFileSync, writeFileSync } from "node:fs"
import process from "node:process"

const artifactPath = "artifacts/framework-parity.json"
const outputPath = "docs/reference/framework-parity-summary.md"
const checkMode = process.argv.includes("--check")

function mark(value) {
  return value ? "yes" : "no"
}

const artifact = JSON.parse(readFileSync(artifactPath, "utf8"))
const families = [...artifact.families].sort((left, right) =>
  left.family.localeCompare(right.family),
)

const lines = [
  "# Framework Parity Summary",
  "",
  "This summary is generated from `artifacts/framework-parity.json`.",
  "",
  "It is a compact human view of the current generated parity artifact. Do not edit this file by hand; run `pnpm parity:summary` instead.",
  "",
  "| Family | React | Vue | Metadata parity | Contract coverage | Story coverage |",
  "|---|---|---|---|---|---|",
]

for (const family of families) {
  lines.push(
    `| ${family.family} | ${mark(family.react)} | ${mark(family.vue)} | ${mark(family.metadataParity)} | ${mark(family.contractCoverage)} | ${mark(family.storyCoverage)} |`,
  )
}

lines.push(
  "",
  "## Validation",
  "",
  "```bash",
  "pnpm parity:summary:check",
  "pnpm validate:docs",
  "```",
  "",
)

const nextContent = `${lines.join("\n")}`

if (checkMode) {
  const currentContent = readFileSync(outputPath, "utf8")
  if (currentContent !== nextContent) {
    console.error(`${outputPath} is stale. Run pnpm parity:summary.`)
    process.exit(1)
  }

  console.log(`✓ Framework parity summary is up to date (${families.length} families)`)
  process.exit(0)
}

writeFileSync(outputPath, nextContent, "utf8")
console.log(`✓ Wrote ${outputPath} (${families.length} families)`)
